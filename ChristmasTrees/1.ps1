for ($i = 3; $i -le 9; $i++) {
    for ($t = 1; $t -lt $i * 2; $t += 2) {
        ' ' * ($i - $t / 2 + .5) + '*' * $t
    }
    ' ' * $i + "*`n"
}

for ($i = 3; $i -le 5; $i++) {
    for ($t = .5; $t -lt $i; $t++) {
        '.' * ($i - $t + .5) + '*' * ($t * 2)
    }
    '.' * $i + "*`n"
}

for ($i = 3; $i -le 5; $i++) {
    $j = $i
    for ($t = 1; $t -lt $i * 2; $t += 2) {
        '.' * $j-- + '*' * $t
    }
    '.' * $i + "*`n"
}

for ($j = $i = 3; $i -le 5; $j = ++$i) {
    for ($t = 1; $j -ge 1; $t += 2) {
        '.' * $j-- + '*' * $t
    }
    '.' * $i + "*`n"
}

(3..9) | % {
    for ($t = .5; $t -lt $_; $t++) {
        '.' * ($_ - $t + .5) + '*' * ($t * 2)
    }
    '.' * $_ + "*`n"
}
